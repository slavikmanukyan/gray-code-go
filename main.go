package main

import (
	"fmt"
)

func convertToGray(num uint) uint {
	return num ^ (num >> 1)
}

func main() {
	fmt.Println("| Decimal | Binary |  Gray |")
	for i := 0; i <= 31; i++ {
		fmt.Printf("| %7d |  %05b | %05b |\n", i, i, convertToGray(uint(i)))
	}
}

